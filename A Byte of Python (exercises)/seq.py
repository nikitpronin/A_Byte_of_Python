shoplist=['apples','mango','carrot','bananas']
name='apple watch'
#indexing
print('First el - ',shoplist[0])
print('Second el - ',shoplist[1])
print('Minus the first el - ', shoplist[-1])
print('Symbol 0 - ',name[0])

#clipping
print('1-3 elements: ',shoplist[1:3])
print('2 to end elements: ', shoplist[2:])
print('1 to -1 elements: ', shoplist[1:-1])
print('all elements: ',shoplist[:])

#string clipping

print('Symbols 1 to 3:', name[1:3])
print ('Symbols 2 to end:', name[2:])
print ('All symbols:',name[:])
      
